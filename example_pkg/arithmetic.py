def add(x, y):
    """The sum of two numbers.

    Returns the sum `x + y`.

    Parameters
    ----------
    x : number
        The first addend of the sum.
    y : number
        The second addend of the sum.

    Returns
    -------
    number
        The sum of the two numbers. Type depends on type of x and y.
    """
    return x + y


def subtract(x, y):
    """The difference of two numbers.

    Returns the difference `x - y`.

    Parameters
    ----------
    x : number
        The minuend of the difference.
    y : number
        The subtrahend of the difference.

    Returns
    -------
    number
        The difference of the two numbers. Type depends on type of x and y.
    """
    return x - y


def multiply(x, y):
    """The product of two numbers.

    Returns the product `x * y`.

    Parameters
    ----------
    x : number
        One number to be multiplied by the other.
    y : number
        The other number to be multiplied by the other.

    Returns
    -------
    number
        The product of the two numbers. Type depends on type of x and y.
    """
    return x * y


def divide(x, y):
    """The ratio of two numbers.

    Returns the ratio `x / y`.

    Parameters
    ----------
    x : number
        The numerator of the ratio.
    y : number
        The denominator of the ratio.

    Returns
    -------
    number
        The ratio of the two numbers. Type depends on type of x and y.
    """
    return x / y
